-- 月初リセット用。Vercel Cron / Supabase Scheduled Function などから呼び出す。
CREATE OR REPLACE FUNCTION reset_monthly_usage_minutes()
RETURNS VOID AS $$
BEGIN
    UPDATE users_cache
    SET monthly_usage_minutes = 0,
        updated_at = NOW()
    WHERE deleted_at IS NULL;
END;
$$ LANGUAGE plpgsql;
