-- 6. 自動チェックアウト用バッチ関数
--    チェックイン後 15時間を超えた在室中レコードを自動退室
-- ==========================================
CREATE OR REPLACE FUNCTION auto_checkout_old_logs(auto_checkout_time_threshold TIMESTAMPTZ)
RETURNS VOID AS $$
BEGIN
    WITH updated_logs AS (
        UPDATE usage_logs
        SET checked_out_at = checked_in_at + INTERVAL '15 hours',
            auto_checked_out = TRUE,
            admin_confirmed = FALSE
        WHERE checked_out_at IS NULL
          AND deleted_at     IS NULL
          AND checked_in_at  < auto_checkout_time_threshold
        RETURNING id, student_id, department, grade, name
    )
    INSERT INTO notifications (
        type,
        usage_log_id,
        student_number,
        department,
        grade,
        name,
        message,
        is_read,
        is_acknowledged
    )
    SELECT
        'auto_checkout',
        id,
        student_id,
        department,
        grade,
        name,
        student_id || ' ' || name || ' さんが15時間経過により自動退室になりました。',
        FALSE,
        FALSE
    FROM updated_logs
    ON CONFLICT DO NOTHING;
END;
$$ LANGUAGE plpgsql;


-- ==========================================
-- 7. 論理削除レコードの物理削除用バッチ関数
--    削除から30日を超えたレコードを物理削除
-- ==========================================
CREATE OR REPLACE FUNCTION purge_old_deleted_records()
RETURNS VOID AS $$
BEGIN
    DELETE FROM usage_logs
    WHERE deleted_at IS NOT NULL
      AND deleted_at < NOW() - INTERVAL '30 days';

    DELETE FROM users_cache
    WHERE deleted_at IS NOT NULL
      AND deleted_at < NOW() - INTERVAL '30 days';

    DELETE FROM department_classes
    WHERE department_id IN (
        SELECT id FROM departments_master
        WHERE deleted_at IS NOT NULL
          AND deleted_at < NOW() - INTERVAL '30 days'
    );

    DELETE FROM departments_master
    WHERE deleted_at IS NOT NULL
      AND deleted_at < NOW() - INTERVAL '30 days';
END;
$$ LANGUAGE plpgsql;

